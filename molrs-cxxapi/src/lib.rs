/// Read the first frame of a Zarr-stored MolRec into a fresh `FrameRef`.
///
/// Used by Atomiverse checkpoint reload (`cpu::ZarrReader`): stage 1 of
/// a long bench writes its end-state via [`molrec_write_zarr`], then
/// subsequent debug iterations of stage 2+ call this to skip stage 1.
///
/// The returned `FrameRef` is populated via `with_mut` on a fresh
/// standalone store — readers (`frame_column_f64`, `frame_simbox`, etc.)
/// see exactly the columns and simbox that were stored.
#[cfg(feature = "zarr")]
fn molrec_read_zarr_first_frame(path: &str) -> Box<FrameRef> {
    let molrec = read_molrec_file(path).expect("molrec_read_zarr_first_frame: read");
    let frame = molrec
        .frame_at(0)
        .expect("molrec_read_zarr_first_frame: empty trajectory");
    let inner = molrs_ffi::FrameRef::new_standalone();
    inner
        .with_mut(|f| {
            *f = frame;
        })
        .expect("molrec_read_zarr_first_frame: populate");
    Box::new(FrameRef(inner))
}

/// Atomic number for a chemical symbol — inverse of [`symbol_for_z`].
///
/// Reuses the same table (no duplicate periodic table). No fallback:
/// panics on an unrecognized symbol, matching Atomiverse's "lookup tables
/// throw on unsupported input" convention.
fn z_for_symbol(sym: &str) -> i32 {
    (1..=118)
        .find(|&z| symbol_for_z(z).eq_ignore_ascii_case(sym))
        .unwrap_or_else(|| panic!("z_for_symbol: unknown element symbol '{sym}'"))
}

/// Read the first frame of an (ext)XYZ file into a materialize-ready `FrameRef`.
///
/// All parsing (atom table, `Lattice="..."` -> simbox) is done by the molrs
/// core ExtXYZ reader. The species/element symbol column is mapped to a `type`
/// (atomic number Z) i32 column so the result satisfies the schema that
/// `cpu::materialize` requires (`atoms.{x,y,z,type}`).
fn xyz_read_first_frame(path: &str) -> Box<FrameRef> {
    let frame = molrs::io::data::xyz::read_xyz_frame(path).expect("xyz_read_first_frame: read");
    let inner = molrs_ffi::FrameRef::new_standalone();
    inner
        .with_mut(|f| {
            *f = frame;
        })
        .expect("xyz_read_first_frame: populate");
    let mut fref = Box::new(FrameRef(inner));

    // Derive atoms.type (Z) from the species/element symbol column.
    let cols = frame_block_columns(&fref, "atoms");
    let sym_col = if cols.iter().any(|c| c == "species") {
        "species"
    } else if cols.iter().any(|c| c == "element") {
        "element"
    } else {
        panic!("xyz_read_first_frame: atoms block has no species/element column");
    };
    let syms = frame_column_str(&fref, "atoms", sym_col);
    let zs: Vec<i32> = syms.iter().map(|s| z_for_symbol(s)).collect();
    frame_set_column_i32(&mut fref, "atoms", "type", &zs);
    fref
}

fn molrec_print_summary(rec: &AtvMolRec) {
    let mr = &rec.rec;
    let n = mr.count_frames();
    if n == 0 {
        return;
    }
    let frame = match mr.frame_at(n - 1) {
        Some(f) => f,
        None => return,
    };
    let meta = &frame.meta;
    let method = meta.get("method").map(|s| s.as_str()).unwrap_or("?");
    let energy: f64 = meta
        .get("energy")
        .and_then(|s| s.parse().ok())
        .unwrap_or(0.0);
    let iters: i32 = meta
        .get("iterations")
        .and_then(|s| s.parse().ok())
        .unwrap_or(0);
    let na: i32 = meta
        .get("n_alpha")
        .and_then(|s| s.parse().ok())
        .unwrap_or(0);
    let nb: i32 = meta.get("n_beta").and_then(|s| s.parse().ok()).unwrap_or(0);
    let sc: f64 = meta
        .get("spin_contamination")
        .and_then(|s| s.parse().ok())
        .unwrap_or(0.0);
    println!();
    println!(
        "\u{2500}\u{2500}\u{2500} {} Molecular Record \u{2500}{}",
        method,
        "\u{2500}".repeat(39)
    );
    println!("  Energy:         {:+.10} Hartree", energy);
    println!("  Iterations:     {}", iters);
    if method == "UHF" {
        println!(
            "  Electrons:      {} alpha, {} beta  (2S+1 = {})",
            na,
            nb,
            na - nb + 1
        );
        println!("  Spin contam.:   {:.6}  (<S^2> - S*(S+1))", sc);
    } else {
        println!("  Electrons:      {}  (closed-shell)", na + nb);
    }
    // Use FrameAccess for column access
    use molrs::store::frame_access::FrameAccess;
    if let Some(charges) = frame.get_float("atoms", "charge") {
        let spins = frame.get_float("atoms", "spin_density");
        let elems = frame.get_string("atoms", "element");
        println!("\n  Mulliken population analysis:");
        println!(
            "  {:<4}  {:<4}  {:>10}  {:>10}",
            "Atom", "Sym", "Charge", "SpinDens"
        );
        println!(
            "  {:<4}  {:<4}  {:>10}  {:>10}",
            "----", "---", "----------", "----------"
        );
        let mut tot = 0.0 as F;
        for a in 0..charges.len() {
            let sym = elems
                .as_ref()
                .and_then(|e| e.as_slice())
                .and_then(|s| s.get(a))
                .map(|s| s.as_str())
                .unwrap_or("X");
            let q = charges[a];
            let sp = spins.as_ref().map(|sv| sv[a]).unwrap_or(0.0 as F);
            println!("  {:<4}  {:<4}  {:+10.6}  {:+10.6}", a, sym, q, sp);
            tot += q;
        }
        println!("  {:<4}  {:<4}  {:+10.6}", "Tot", "", tot);
    }
    println!("{}\n", "\u{2500}".repeat(57));
}

