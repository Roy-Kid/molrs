//! CXX bridge exposing molrs I/O and analysis to Atomiverse (C++).
//!
//! Zero-copy I/O: C++ slices → FrameView (borrowed) → write_xyz_frame.
//! Owned Frame only built when persisting to MolRec (Zarr).

