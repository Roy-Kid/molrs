// ── Staging — holds borrowed references to C++ data between FFI calls ────────

struct Staging {
    symbols: Vec<String>,
    x: Vec<F>,
    y: Vec<F>,
    z: Vec<F>,
    box_mat: [F; 9],
    fields: Vec<(String, Vec<F>)>,
    scalars: Vec<(String, f64)>,
    strings: Vec<(String, String)>,
}

impl Default for Staging {
    fn default() -> Self {
        Self {
            symbols: Vec::new(),
            x: Vec::new(),
            y: Vec::new(),
            z: Vec::new(),
            box_mat: [0.0 as F; 9],
            fields: Vec::new(),
            scalars: Vec::new(),
            strings: Vec::new(),
        }
    }
}

impl Staging {
    /// Write staging data via FrameView (zero-copy).
    fn write_view<W: Write>(&self, w: &mut W) {
        let mut atoms = BlockView::new();
        atoms.insert(
            "element",
            ColumnView::String(ArrayView1::from(&self.symbols[..]).into_dyn()),
        );
        atoms.insert(
            "x",
            ColumnView::Float(ArrayView1::from(&self.x[..]).into_dyn()),
        );
        atoms.insert(
            "y",
            ColumnView::Float(ArrayView1::from(&self.y[..]).into_dyn()),
        );
        atoms.insert(
            "z",
            ColumnView::Float(ArrayView1::from(&self.z[..]).into_dyn()),
        );
        for (name, vals) in &self.fields {
            atoms.insert(
                name.as_str(),
                ColumnView::Float(ArrayView1::from(&vals[..]).into_dyn()),
            );
        }

        let h = Array2::from_shape_vec((3, 3), self.box_mat.to_vec()).unwrap();
        let simbox = SimBox::new(h, Array1::zeros(3), [true, true, true]).ok();
        let meta = {
            let mut m = HashMap::new();
            for (k, v) in &self.scalars {
                m.insert(k.clone(), format!("{}", v));
            }
            for (k, v) in &self.strings {
                m.insert(k.clone(), v.clone());
            }
            m
        };

        let mut map = HashMap::new();
        map.insert("atoms", atoms);
        let view = FrameView::from_parts(map, simbox.as_ref(), &meta);
        write_xyz_frame(w, &view).unwrap();
    }

    /// Consume staging into an owned Frame for MolRec storage (one copy).
    fn take_frame(&mut self) -> Frame {
        let mut atoms = Block::new();
        atoms
            .insert(
                "element",
                Array1::from_vec(std::mem::take(&mut self.symbols)).into_dyn() as ArrayD<String>,
            )
            .unwrap();
        atoms
            .insert(
                "x",
                Array1::from_vec(std::mem::take(&mut self.x)).into_dyn(),
            )
            .unwrap();
        atoms
            .insert(
                "y",
                Array1::from_vec(std::mem::take(&mut self.y)).into_dyn(),
            )
            .unwrap();
        atoms
            .insert(
                "z",
                Array1::from_vec(std::mem::take(&mut self.z)).into_dyn(),
            )
            .unwrap();
        for (name, vals) in self.fields.drain(..) {
            atoms
                .insert(name.as_str(), Array1::from_vec(vals).into_dyn())
                .unwrap();
        }
        let mut frame = Frame::new();
        frame.insert("atoms", atoms);
        let h = Array2::from_shape_vec((3, 3), self.box_mat.to_vec()).unwrap();
        if let Ok(sb) = SimBox::new(h, Array1::zeros(3), [true, true, true]) {
            frame.simbox = Some(sb);
        }
        for (k, v) in self.scalars.drain(..) {
            frame.meta.insert(k, format!("{}", v));
        }
        for (k, v) in self.strings.drain(..) {
            frame.meta.insert(k, v);
        }
        self.box_mat = [0.0 as F; 9];
        frame
    }
}

// ── AtvMolRec ────────────────────────────────────────────────────────────────

pub struct AtvMolRec {
    rec: MolRec,
    staging: Staging,
    n_frames: i32,
}

impl AtvMolRec {
    fn new() -> Self {
        Self {
            rec: MolRec::new(Frame::new()),
            staging: Staging::default(),
            n_frames: 0,
        }
    }
}

// ── MolRec bridge ────────────────────────────────────────────────────────────

fn molrec_new() -> Box<AtvMolRec> {
    Box::new(AtvMolRec::new())
}

fn molrec_set_geometry(
    rec: &mut AtvMolRec,
    type_id: &[i32],
    x: &[F],
    y: &[F],
    z: &[F],
    box_mat: &[F],
) {
    let n = type_id.len();
    let s = &mut rec.staging;
    s.symbols = type_id
        .iter()
        .map(|&z| symbol_for_z(z).to_string())
        .collect();
    s.x = x[..n].to_vec();
    s.y = y[..n].to_vec();
    s.z = z[..n].to_vec();
    if box_mat.len() >= 9 {
        s.box_mat.copy_from_slice(&box_mat[..9]);
    }
}

fn molrec_add_field(rec: &mut AtvMolRec, name: &str, values: &[F]) {
    rec.staging.fields.push((name.to_string(), values.to_vec()));
}
fn molrec_add_scalar(rec: &mut AtvMolRec, name: &str, value: f64) {
    rec.staging.scalars.push((name.to_string(), value));
}
fn molrec_add_string(rec: &mut AtvMolRec, name: &str, value: &str) {
    rec.staging
        .strings
        .push((name.to_string(), value.to_string()));
}

fn molrec_commit_frame(rec: &mut AtvMolRec) {
    let frame = rec.staging.take_frame(); // one copy for persistence
    rec.rec.add_frame(frame);
    rec.n_frames += 1;
}

fn molrec_clear(rec: &mut AtvMolRec) {
    rec.rec = MolRec::new(Frame::new());
    rec.staging = Staging::default();
    rec.n_frames = 0;
}

fn molrec_n_frames(rec: &AtvMolRec) -> i32 {
    rec.n_frames
}

