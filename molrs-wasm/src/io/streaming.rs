//! Streaming trajectory readers for the WASM API.
//!
//! Implements Phase 1 / Task #4 of the streaming-trajectory spec
//! (see `molvis/docs/specs/streaming-trajectory.md`). Unlike the legacy
//! whole-content readers in [`super::reader`], these classes are designed
//! to be driven by a worker that owns:
//!
//! - a *reusable* WASM input buffer ([`allocInputBuffer`](
//!   `WasmLammpsDumpStream::alloc_input_buffer`)) into which the worker
//!   writes a chunk of the source (e.g. `blob.slice(start, end)`),
//! - a chunk-boundary-safe `FrameIndexBuilder` from `molrs-io` that emits
//!   [`FrameIndexEntry`] records as the source flows through it,
//! - a frame-by-frame parser that decodes a single byte range from the
//!   reusable buffer into a [`Frame`] held in
//!   `WasmLammpsDumpStream::output`.
//!
//! Five wasm classes share this exact shape — `WasmLammpsDumpStream`,
//! `WasmXyzStream`, `WasmPdbStream`, `WasmLammpsDataStream`,
//! `WasmSdfStream` — so they are generated by the
//! [`impl_wasm_traj_stream`] macro below. Each only differs in *which*
//! `FrameIndexBuilder` is constructed and *which* `parse_frame_bytes`
//! free function is invoked.
//!
//! # JS lifetime contract
//!
//! Every numeric pointer returned from this module (input buffer,
//! column data, grid array data) lives inside WASM linear memory.
//! `WebAssembly.Memory` may grow on any wasm-bindgen call, which
//! detaches the previous `ArrayBuffer`. Therefore **callers MUST
//! re-derive `new <Typed>Array(memory.buffer, ptr, len)` immediately
//! before each access and MUST NOT hold onto pointers across other
//! wasm calls.** The caller is also responsible for copying the data
//! into a JS-owned typed array before invoking [`parseRangeInInput`](
//! `WasmLammpsDumpStream::parse_range_in_input`) again.

use molrs::block::{Column, DType};
use molrs::frame::Frame as RsFrame;
use molrs_io::streaming::{FrameIndexBuilder, FrameIndexEntry as RsFrameIndexEntry};
use wasm_bindgen::prelude::*;

// Per-format builders + parsers. We re-import each format's
