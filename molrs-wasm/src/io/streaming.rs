/// One frame's location inside the source byte stream.
///
/// `byteOffset` is the absolute byte position of the frame's first byte
/// inside the source (round-tripped through `f64`; the spec caps source
/// size at 1 TB which is well within `Number.MAX_SAFE_INTEGER`).
/// `byteLen` is `u32` — per-frame size never exceeds the worker chunk
/// size plus a small slack.
#[wasm_bindgen(js_name = FrameIndexEntry)]
#[derive(Debug, Clone, Copy)]
pub struct WasmFrameIndexEntry {
    byte_offset: f64,
    byte_len: u32,
}

#[wasm_bindgen(js_class = FrameIndexEntry)]
impl WasmFrameIndexEntry {
    /// Absolute byte offset of this frame inside the source (in bytes).
    #[wasm_bindgen(getter, js_name = byteOffset)]
    pub fn byte_offset(&self) -> f64 {
        self.byte_offset
    }

    /// Size of this frame's encoded byte range, in bytes.
    #[wasm_bindgen(getter, js_name = byteLen)]
    pub fn byte_len(&self) -> u32 {
        self.byte_len
    }
}

impl From<RsFrameIndexEntry> for WasmFrameIndexEntry {
    fn from(e: RsFrameIndexEntry) -> Self {
        // `byte_offset: u64 → f64` is exact for values ≤ 2^53; the
        // streaming protocol caps source size at 1 TB.
        Self {
            byte_offset: e.byte_offset as f64,
            byte_len: e.byte_len,
        }
    }
}

// ---------------------------------------------------------------------------
// Internal column / block enumeration helpers
// ---------------------------------------------------------------------------

/// Stable, sorted index into a parsed frame.
///
/// Block and column ordering inside `molrs::frame::Frame` come from
/// `HashMap`s, which means iteration order is non-deterministic. We
/// snapshot block keys (sorted) and per-block column keys (sorted) at
/// parse time so JS can address them by stable integer indices.
#[derive(Default)]
struct FrameIndex {
    blocks: Vec<BlockIndex>,
    grids: Vec<GridIndex>,
}

struct BlockIndex {
    name: String,
    columns: Vec<String>,
}

struct GridIndex {
    name: String,
    arrays: Vec<String>,
}

impl FrameIndex {
    fn new(frame: &RsFrame) -> Self {
        let mut blocks: Vec<BlockIndex> = frame
            .iter()
            .map(|(name, block)| {
                let mut columns: Vec<String> = block.keys().map(|k| k.to_string()).collect();
                columns.sort();
                BlockIndex {
                    name: name.to_string(),
                    columns,
                }
            })
            .collect();
        blocks.sort_by(|a, b| a.name.cmp(&b.name));

        let mut grids: Vec<GridIndex> = frame
            .grids()
            .map(|(name, grid)| {
                let mut arrays: Vec<String> = grid.keys().map(|k| k.to_string()).collect();
                arrays.sort();
                GridIndex {
                    name: name.to_string(),
                    arrays,
                }
            })
            .collect();
        grids.sort_by(|a, b| a.name.cmp(&b.name));

        Self { blocks, grids }
    }
}

/// Resolved view onto one column inside a parsed `Frame`.
struct ColumnAccess<'a> {
    column: &'a Column,
    name: &'a str,
}

fn column_access<'a>(
    frame: &'a RsFrame,
    index: &'a FrameIndex,
    block_idx: usize,
    col_idx: usize,
) -> Result<ColumnAccess<'a>, JsValue> {
    let block_meta = index.blocks.get(block_idx).ok_or_else(|| {
        JsValue::from_str(&format!("block index {} out of range", block_idx))
    })?;
    let col_name = block_meta.columns.get(col_idx).ok_or_else(|| {
        JsValue::from_str(&format!(
            "column index {} out of range in block {}",
            col_idx, block_meta.name
        ))
    })?;
    let block = frame
        .get(&block_meta.name)
        .ok_or_else(|| JsValue::from_str(&format!("block {} disappeared", block_meta.name)))?;
    let column = block
        .get(col_name)
        .ok_or_else(|| JsValue::from_str(&format!("column {} disappeared", col_name)))?;
    Ok(ColumnAccess {
        column,
        name: col_name.as_str(),
    })
}

fn dtype_str(dtype: DType) -> &'static str {
    match dtype {
        DType::Float => "f64",
        DType::Int => "i32",
        DType::UInt => "u32",
        DType::String => "string",
        // The five streaming formats only emit Float / Int / UInt / String
        // columns; bool / u8 are not produced by any current parser, but if
        // they show up we still report the underlying dtype so the JS side
        // can at least decide to skip the column rather than misinterpret it.
        DType::Bool => "bool",
        DType::U8 => "u8",
    }
}

fn column_total_len(column: &Column) -> usize {
    column.shape().iter().copied().product()
}

