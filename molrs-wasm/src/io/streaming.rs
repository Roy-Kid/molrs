/// Generate a per-format `Wasm*Stream` class.
///
/// Each invocation emits a `#[wasm_bindgen]` struct, a `#[wasm_bindgen]`
/// impl block, and the same set of JS-facing methods. The only thing that
/// varies between formats is the indexer constructor and the
/// `parse_frame_bytes` free function — both passed in as expressions.
macro_rules! impl_wasm_traj_stream {
    (
        struct_name = $struct:ident,
        js_name     = $js:ident,
        new_indexer = $new_indexer:expr,
        parse_fn    = $parse_fn:path,
    ) => {
        #[wasm_bindgen(js_name = $js)]
        pub struct $struct {
            indexer: Option<Box<dyn FrameIndexBuilder>>,
            input_buf: Vec<u8>,
            output: Option<RsFrame>,
            output_index: FrameIndex,
        }

        #[wasm_bindgen(js_class = $js)]
        impl $struct {
            /// Create a new streaming reader. The reusable input buffer
            /// starts empty; call [`allocInputBuffer`] before feeding data.
            #[wasm_bindgen(constructor)]
            #[allow(clippy::new_without_default)]
            pub fn new() -> Self {
                let indexer: Box<dyn FrameIndexBuilder> = Box::new($new_indexer);
                Self {
                    indexer: Some(indexer),
                    input_buf: Vec::new(),
                    output: None,
                    output_index: FrameIndex::default(),
                }
            }

            /// Resize the reusable input buffer so it can hold at least
            /// `len` bytes, and return the WASM linear-memory pointer to
            /// its first byte.
            ///
            /// CALLER MUST RE-DERIVE THIS POINTER AFTER ANY OTHER WASM CALL —
            /// `wasm.memory.buffer` may have been detached by a `memory.grow`.
            #[wasm_bindgen(js_name = allocInputBuffer)]
            pub fn alloc_input_buffer(&mut self, len: usize) -> *mut u8 {
                if self.input_buf.len() < len {
                    self.input_buf.resize(len, 0);
                }
                self.input_buf.as_mut_ptr()
            }

            /// Current capacity (in bytes) of the reusable input buffer.
            #[wasm_bindgen(js_name = inputCapacity)]
            pub fn input_capacity(&self) -> usize {
                self.input_buf.len()
            }

            /// Feed the first `len` bytes of the input buffer into the
            /// indexer at absolute source offset `globalOffset`. Returns
            /// any frame entries that became finalized as a result.
            ///
            /// `globalOffset` is round-tripped through `f64`; per the
            /// streaming protocol the source size is capped at 1 TB
            /// (well within `Number.MAX_SAFE_INTEGER`).
            #[wasm_bindgen(js_name = feedIndexChunk)]
            pub fn feed_index_chunk(
                &mut self,
                global_offset: f64,
                len: usize,
            ) -> Result<Vec<WasmFrameIndexEntry>, JsValue> {
                if global_offset < 0.0 || !global_offset.is_finite() {
                    return Err(JsValue::from_str(
                        "feedIndexChunk: globalOffset must be a non-negative finite number",
                    ));
                }
                if len > self.input_buf.len() {
                    return Err(JsValue::from_str(&format!(
                        "feedIndexChunk: len {} exceeds input buffer capacity {}",
                        len,
                        self.input_buf.len()
                    )));
                }
                let indexer = self
                    .indexer
                    .as_mut()
                    .ok_or_else(|| JsValue::from_str("feedIndexChunk: indexer already finished"))?;
                indexer.feed(&self.input_buf[..len], global_offset as u64);
                Ok(indexer.drain().into_iter().map(Into::into).collect())
            }

            /// Signal end-of-stream; return any trailing frame entry. The
            /// indexer is consumed and further `feedIndexChunk` calls
            /// throw.
            #[wasm_bindgen(js_name = finishIndex)]
            pub fn finish_index(&mut self) -> Result<Vec<WasmFrameIndexEntry>, JsValue> {
                let indexer = self
                    .indexer
                    .take()
                    .ok_or_else(|| JsValue::from_str("finishIndex: indexer already finished"))?;
                let trailing = indexer
                    .finish()
                    .map_err(|e| JsValue::from_str(&format!("finishIndex: {}", e)))?;
                Ok(trailing.into_iter().map(Into::into).collect())
            }

            /// Decode the byte range `[offset, offset + len)` from the
            /// reusable input buffer as a single frame. Replaces any
            /// previously cached output; the caller must extract data
            /// before the next `parseRangeInInput` (the same buffer can
            /// be overwritten in between).
            #[wasm_bindgen(js_name = parseRangeInInput)]
            pub fn parse_range_in_input(
                &mut self,
                offset: usize,
                len: usize,
            ) -> Result<(), JsValue> {
                let end = offset.checked_add(len).ok_or_else(|| {
                    JsValue::from_str("parseRangeInInput: offset + len overflowed usize")
                })?;
                if end > self.input_buf.len() {
                    return Err(JsValue::from_str(&format!(
                        "parseRangeInInput: range {}..{} exceeds input buffer length {}",
                        offset,
                        end,
                        self.input_buf.len()
                    )));
                }
                let bytes = &self.input_buf[offset..end];
                let frame = $parse_fn(bytes)
                    .map_err(|e| JsValue::from_str(&format!("parse error: {}", e)))?;
                self.output_index = FrameIndex::new(&frame);
                self.output = Some(frame);
                Ok(())
            }

            /// Drop the cached output frame, releasing its memory and
            /// invalidating any outstanding `columnPtr*` / `gridArrayPtr*`
            /// pointers. Subsequent extraction calls return 0 / empty
            /// until the next `parseRangeInInput`.
            #[wasm_bindgen(js_name = releaseFrame)]
            pub fn release_frame(&mut self) {
                self.output = None;
                self.output_index = FrameIndex::default();
            }

            // ---------- block / column metadata ----------

            /// Number of blocks in the most recently parsed frame.
            #[wasm_bindgen(js_name = blockCount)]
            pub fn block_count(&self) -> usize {
                self.output_index.blocks.len()
            }

            /// Name of block `blockIdx`, or empty string if out of range.
            #[wasm_bindgen(js_name = blockName)]
            pub fn block_name(&self, block_idx: usize) -> String {
                self.output_index
                    .blocks
                    .get(block_idx)
                    .map(|b| b.name.clone())
                    .unwrap_or_default()
            }

            /// Number of columns in block `blockIdx`.
            #[wasm_bindgen(js_name = columnCount)]
            pub fn column_count(&self, block_idx: usize) -> usize {
                self.output_index
                    .blocks
                    .get(block_idx)
                    .map(|b| b.columns.len())
                    .unwrap_or(0)
            }

            /// Column name at `(blockIdx, colIdx)`, or empty string if
            /// either index is out of range.
            #[wasm_bindgen(js_name = columnName)]
            pub fn column_name(&self, block_idx: usize, col_idx: usize) -> String {
                self.output_index
                    .blocks
                    .get(block_idx)
                    .and_then(|b| b.columns.get(col_idx))
                    .cloned()
                    .unwrap_or_default()
            }

            /// Column dtype string at `(blockIdx, colIdx)`. One of `"f64"`,
            /// `"u32"`, `"i32"`, `"string"` — or `"bool"` / `"u8"` if a
            /// future parser emits those (current streaming formats do not).
            /// Empty string if the index is out of range.
            #[wasm_bindgen(js_name = columnDtype)]
            pub fn column_dtype(&self, block_idx: usize, col_idx: usize) -> String {
                let Some(frame) = self.output.as_ref() else {
                    return String::new();
                };
                let Ok(access) = column_access(frame, &self.output_index, block_idx, col_idx)
                else {
                    return String::new();
                };
                dtype_str(access.column.dtype()).to_string()
            }

            /// Total flat element count of the column. For multi-dimensional
            /// columns (e.g. an Nx3 positions array) this is the product of
            /// all axes — i.e. the length of the slice the matching
            /// `columnPtr*` points at.
            #[wasm_bindgen(js_name = columnLen)]
            pub fn column_len(&self, block_idx: usize, col_idx: usize) -> usize {
                let Some(frame) = self.output.as_ref() else {
                    return 0;
                };
                let Ok(access) = column_access(frame, &self.output_index, block_idx, col_idx)
                else {
                    return 0;
                };
                column_total_len(access.column)
            }

            // ---------- numeric column pointers ----------
            //
            // CALLER MUST COPY the data into a JS-owned typed array before
            // any other wasm call: a subsequent allocInputBuffer / parse /
            // anything that triggers a `memory.grow` will detach
            // `wasm.memory.buffer`.

            /// Pointer to the contiguous `f64` slice backing this column,
            /// or 0 (null) if the column is missing or has the wrong
            /// dtype.
            ///
            /// **Lifetime**: valid only until the next non-trivial wasm
            /// call. See the module-level memory-grow contract.
            #[wasm_bindgen(js_name = columnPtrF64)]
            pub fn column_ptr_f64(&self, block_idx: usize, col_idx: usize) -> *const f64 {
                let Some(frame) = self.output.as_ref() else {
                    return std::ptr::null();
                };
                let Ok(access) = column_access(frame, &self.output_index, block_idx, col_idx)
                else {
                    return std::ptr::null();
                };
                let Some(arr) = access.column.as_float() else {
                    return std::ptr::null();
                };
                arr.as_slice_memory_order()
                    .map(|s| s.as_ptr())
                    .unwrap_or(std::ptr::null())
            }

            /// Pointer to the contiguous `u32` slice backing this column,
            /// or 0 (null) if the column is missing or has the wrong
            /// dtype.
            ///
            /// **Lifetime**: valid only until the next non-trivial wasm
            /// call. See the module-level memory-grow contract.
            #[wasm_bindgen(js_name = columnPtrU32)]
            pub fn column_ptr_u32(&self, block_idx: usize, col_idx: usize) -> *const u32 {
                let Some(frame) = self.output.as_ref() else {
                    return std::ptr::null();
                };
                let Ok(access) = column_access(frame, &self.output_index, block_idx, col_idx)
                else {
                    return std::ptr::null();
                };
                let Some(arr) = access.column.as_uint() else {
                    return std::ptr::null();
                };
                arr.as_slice_memory_order()
                    .map(|s| s.as_ptr())
                    .unwrap_or(std::ptr::null())
            }

            /// Pointer to the contiguous `i32` slice backing this column,
            /// or 0 (null) if the column is missing or has the wrong
            /// dtype.
            ///
            /// **Lifetime**: valid only until the next non-trivial wasm
            /// call. See the module-level memory-grow contract.
            #[wasm_bindgen(js_name = columnPtrI32)]
            pub fn column_ptr_i32(&self, block_idx: usize, col_idx: usize) -> *const i32 {
                let Some(frame) = self.output.as_ref() else {
                    return std::ptr::null();
                };
                let Ok(access) = column_access(frame, &self.output_index, block_idx, col_idx)
                else {
                    return std::ptr::null();
                };
                let Some(arr) = access.column.as_int() else {
                    return std::ptr::null();
                };
                arr.as_slice_memory_order()
                    .map(|s| s.as_ptr())
                    .unwrap_or(std::ptr::null())
            }

            /// Copy a string column out by value. Returns an empty array
            /// if the column is missing or not a string column.
            #[wasm_bindgen(js_name = columnStrings)]
            pub fn column_strings(&self, block_idx: usize, col_idx: usize) -> Vec<String> {
                let Some(frame) = self.output.as_ref() else {
                    return Vec::new();
                };
                let Ok(access) = column_access(frame, &self.output_index, block_idx, col_idx)
                else {
                    return Vec::new();
                };
                let Some(arr) = access.column.as_string() else {
                    return Vec::new();
                };
                let _ = access.name; // silence unused-name warning
                arr.iter().cloned().collect()
            }

            // ---------- simbox ----------

            /// Box H-matrix as a length-9 `Float64Array` (column-major:
            /// `[col0_x, col0_y, col0_z, col1_x, col1_y, col1_z, col2_x, col2_y, col2_z]`),
            /// or `undefined` if the frame has no simbox.
            ///
            /// Returned by VALUE — the typed array is owned by JS and is
            /// stable across subsequent wasm calls.
            #[wasm_bindgen(js_name = simboxH)]
            pub fn simbox_h(&self) -> Option<Vec<f64>> {
                let frame = self.output.as_ref()?;
                let sb = frame.simbox.as_ref()?;
                let h = sb.h_view();
                // Column-major flatten: H is stored as a 3x3 matrix where
                // h[[i, j]] is row i, column j. The wire format the spec
                // uses puts column 0 first.
                let mut out = Vec::with_capacity(9);
                for j in 0..3 {
                    for i in 0..3 {
                        out.push(h[[i, j]]);
                    }
                }
                Some(out)
            }

            /// Box origin as a length-3 `Float64Array`, or `undefined` if
            /// the frame has no simbox.
            #[wasm_bindgen(js_name = simboxOrigin)]
            pub fn simbox_origin(&self) -> Option<Vec<f64>> {
                let frame = self.output.as_ref()?;
                let sb = frame.simbox.as_ref()?;
                Some(sb.origin_view().iter().copied().collect())
            }

            /// Per-axis PBC flags as a length-3 `Uint8Array` (`1` = periodic,
            /// `0` = open), or `undefined` if the frame has no simbox.
            ///
            /// (wasm-bindgen does not support `[bool; 3]` returns natively,
            /// hence the `Uint8Array` wire format. The spec calls for
            /// `[boolean, boolean, boolean]`; the JS side is expected to
            /// `Boolean(arr[i])` each entry.)
            #[wasm_bindgen(js_name = simboxPbc)]
            pub fn simbox_pbc(&self) -> Option<Vec<u8>> {
                let frame = self.output.as_ref()?;
                let sb = frame.simbox.as_ref()?;
                let pbc = sb.pbc();
                Some(vec![pbc[0] as u8, pbc[1] as u8, pbc[2] as u8])
            }

            // ---------- grids ----------

            /// Number of named grids attached to the current frame.
            #[wasm_bindgen(js_name = gridCount)]
            pub fn grid_count(&self) -> usize {
                self.output_index.grids.len()
            }

            /// Name of grid `gridIdx`, or empty string if out of range.
            #[wasm_bindgen(js_name = gridName)]
            pub fn grid_name(&self, grid_idx: usize) -> String {
                self.output_index
                    .grids
                    .get(grid_idx)
                    .map(|g| g.name.clone())
                    .unwrap_or_default()
            }

            /// Grid dimensions `[nx, ny, nz]` as a length-3 `Uint32Array`.
            #[wasm_bindgen(js_name = gridShape)]
            pub fn grid_shape(&self, grid_idx: usize) -> Vec<u32> {
                let Some(frame) = self.output.as_ref() else {
                    return Vec::new();
                };
                let Some(meta) = self.output_index.grids.get(grid_idx) else {
                    return Vec::new();
                };
                let Some(grid) = frame.get_grid(&meta.name) else {
                    return Vec::new();
                };
                vec![grid.dim[0] as u32, grid.dim[1] as u32, grid.dim[2] as u32]
            }

            /// Grid origin (Cartesian, Å) as a length-3 `Float64Array`.
            #[wasm_bindgen(js_name = gridOrigin)]
            pub fn grid_origin(&self, grid_idx: usize) -> Vec<f64> {
                let Some(frame) = self.output.as_ref() else {
                    return Vec::new();
                };
                let Some(meta) = self.output_index.grids.get(grid_idx) else {
                    return Vec::new();
                };
                let Some(grid) = frame.get_grid(&meta.name) else {
                    return Vec::new();
                };
                grid.origin.to_vec()
            }

            /// Grid lattice cell as a length-9 `Float64Array` (column-major:
            /// `[col0_x, col0_y, col0_z, col1_x, col1_y, col1_z, col2_x, col2_y, col2_z]`).
            #[wasm_bindgen(js_name = gridCell)]
            pub fn grid_cell(&self, grid_idx: usize) -> Vec<f64> {
                let Some(frame) = self.output.as_ref() else {
                    return Vec::new();
                };
                let Some(meta) = self.output_index.grids.get(grid_idx) else {
                    return Vec::new();
                };
                let Some(grid) = frame.get_grid(&meta.name) else {
                    return Vec::new();
                };
                // `cell[i]` is column i; flatten column-major.
                let mut out = Vec::with_capacity(9);
                for col in 0..3 {
                    for row in 0..3 {
                        out.push(grid.cell[col][row]);
                    }
                }
                out
            }

            /// Grid PBC flags as a length-3 `Uint8Array` (`1` = periodic,
            /// `0` = open).
            #[wasm_bindgen(js_name = gridPbc)]
            pub fn grid_pbc(&self, grid_idx: usize) -> Vec<u8> {
                let Some(frame) = self.output.as_ref() else {
                    return Vec::new();
                };
                let Some(meta) = self.output_index.grids.get(grid_idx) else {
                    return Vec::new();
                };
                let Some(grid) = frame.get_grid(&meta.name) else {
                    return Vec::new();
                };
                vec![grid.pbc[0] as u8, grid.pbc[1] as u8, grid.pbc[2] as u8]
            }

            /// Number of named scalar arrays in grid `gridIdx`.
            #[wasm_bindgen(js_name = gridArrayCount)]
            pub fn grid_array_count(&self, grid_idx: usize) -> usize {
                self.output_index
                    .grids
                    .get(grid_idx)
                    .map(|g| g.arrays.len())
                    .unwrap_or(0)
            }

            /// Name of scalar array `(gridIdx, arrayIdx)`, or empty string
            /// if either index is out of range.
            #[wasm_bindgen(js_name = gridArrayName)]
            pub fn grid_array_name(&self, grid_idx: usize, array_idx: usize) -> String {
                self.output_index
                    .grids
                    .get(grid_idx)
                    .and_then(|g| g.arrays.get(array_idx))
                    .cloned()
                    .unwrap_or_default()
            }

            /// Pointer to the contiguous `f64` slice backing the grid
            /// scalar field, or 0 (null) if either index is out of range.
            ///
            /// **Lifetime**: valid only until the next non-trivial wasm
            /// call. See the module-level memory-grow contract.
            #[wasm_bindgen(js_name = gridArrayPtrF64)]
            pub fn grid_array_ptr_f64(
                &self,
                grid_idx: usize,
                array_idx: usize,
            ) -> *const f64 {
                let Some(frame) = self.output.as_ref() else {
                    return std::ptr::null();
                };
                let Some(meta) = self.output_index.grids.get(grid_idx) else {
                    return std::ptr::null();
                };
                let Some(name) = meta.arrays.get(array_idx) else {
                    return std::ptr::null();
                };
                let Some(grid) = frame.get_grid(&meta.name) else {
                    return std::ptr::null();
                };
                grid.get_raw(name)
                    .map(|s| s.as_ptr())
                    .unwrap_or(std::ptr::null())
            }

            /// Length (number of `f64` elements) of the grid scalar field.
            #[wasm_bindgen(js_name = gridArrayLen)]
            pub fn grid_array_len(&self, grid_idx: usize, array_idx: usize) -> usize {
                let Some(frame) = self.output.as_ref() else {
                    return 0;
                };
                let Some(meta) = self.output_index.grids.get(grid_idx) else {
                    return 0;
                };
                let Some(name) = meta.arrays.get(array_idx) else {
                    return 0;
                };
                let Some(grid) = frame.get_grid(&meta.name) else {
                    return 0;
                };
                grid.get_raw(name).map(|s| s.len()).unwrap_or(0)
            }
        }
    };
}

